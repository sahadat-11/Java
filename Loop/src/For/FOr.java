package For;

public class FOr {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
        for (int i = 1; i <= 5; i++) {
            System.out.println("Hello World");
        }
        for (int i=7; i!=0; i--){

            System.out.println(i);

        }
    }
}
