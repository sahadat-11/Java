package Operator;

public class relational {
    public static void main(String[] args) {
        // Comparison operators return ture or false;
        int a = 10;
        int b = 3;
        int c = 5;
        System.out.println("a > b: " + (a > b));
        System.out.println("a < b: " + (a < b));
        System.out.println("a >= b: " + (a >= b));
        System.out.println("a <= b: " + (a <= b));
        System.out.println("a == c: " + (a == c));
        System.out.println("a != c: " + (a != c));
    }
}
