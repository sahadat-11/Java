package Basic;

public class Literals {
    public static void main(String[] args) {
        int a = 3, b = 5, c = 6; // 3, 5, 6 are literals;
        System.out.println("a = " + a + " b = " + b + " c = " + c);
        System.out.println("sum the numbers: " + (a + b + c)); // sum
        byte age = 34;
        System.out.println(age);
    }
}
